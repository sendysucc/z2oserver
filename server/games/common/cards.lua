local module = {}

local cards = {
    0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c,0x0d,        --方块 A - K
    0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c,0x1d,        --梅话 A - K
    0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x2a, 0x2b, 0x2c,0x2d,        --红桃 A - K
    0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39, 0x3a, 0x3b, 0x3c,0x3d,        --黑桃 A - K
    0x4e, 0x4f,                                                                         --大小 joker
}

local function getcards(num)
    local card = {}

    for i = 1, num do
        table.insert( card, cards[i] )
    end

    --wash cards
    for i = 1, num do
        local j = math.random(1, num)
        card[i],card[j] = card[j],card[i]
    end
    
    return card
end

module.getcards_without_joker = function()
    return getcards(52)
end

module.getcards_with_joker = function()
    return getcards(54)
end

math.randomseed(tostring(os.time()):reverse():sub(1, 7))

return module